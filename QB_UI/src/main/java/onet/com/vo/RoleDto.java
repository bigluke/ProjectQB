public class RoleDto {
	
	private String role_desc;
	private String role_code;
	private String role_admin;
	private String role_teacher;
	private String role_student;
	private String role_member;
	
	public String getRole_desc() {
		return role_desc;
	}
	public void setRole_desc(String role_desc) {
		this.role_desc = role_desc;
	}
	public String getRole_code() {
		return role_code;
	}
	public void setRole_code(String role_code) {
		this.role_code = role_code;
	}
	public String getRole_admin() {
		return role_admin;
	}
	public void setRole_admin(String role_admin) {
		this.role_admin = role_admin;
	}
	public String getRole_teacher() {
		return role_teacher;
	}
	public void setRole_teacher(String role_teacher) {
		this.role_teacher = role_teacher;
	}
	public String getRole_student() {
		return role_student;
	}
	public void setRole_student(String role_student) {
		this.role_student = role_student;
	}
	public String getRole_member() {
		return role_member;
	}
	public void setRole_member(String role_member) {
		this.role_member = role_member;
	}
	
	
}
